    public void Deplace(){
        try{
            int row = Table.getSelectedRow();
            this.test = (Table.getModel().getValueAt(row, 0).toString());
            String rqt = "select * from materiel where num_mat = '"+test+"' ";
            ps = conn.prepareStatement(rqt);
            rs = ps.executeQuery();
            if(rs.next()){
                String t1 = rs.getString("num_mat");
                numtxt.setText(t1);
                String t2 = rs.getString("des");
                destxt.setText(t2);
                String t3 = rs.getString("car");
                cartxt.setText(t3);
                String t4 = rs.getString("etat");
                etatxt.setSelectedItem(t4);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }
    
    public void clean(){
        numtxt.setText("");
        destxt.setText("");
        cartxt.setText("");
        etatxt.setSelectedItem("Mauvais");

    }
    
    
    
    
    
