    public void Affichage(){
        try {
            String sql = "select * from materiel";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            Table.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            System.out.println(e);
        }
    }
    
    
